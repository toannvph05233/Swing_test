    private void showDetail() {
        HocSinh hs=lstHocSinh.get(index);
        txtHoTen.setText(hs.getHoten());
        txtTuoi.setText(hs.getTuoi()+"");
        cboNgoaiNgu.setSelectedItem(hs.getNgoaiNgu());
        lblCurrent.setText((index +1) +"/"+lstHocSinh.size());//hiển thị vị trí hiện tại
        tblHocSinh.setRowSelectionInterval(index, index);//table chọn dòng index
    }

    private void fillToTable() {
        model.setRowCount(0);
        for(HocSinh x:lstHocSinh){
            model.addRow(new Object[]{x.getHoten(),x.getTuoi(),x.getNgoaiNgu()});
        }
    }
    private void clearForm() {   
        txtHoTen.setText("");
        txtTuoi.setText("");
        cboNgoaiNgu.setSelectedIndex(0);
        lblCurrent.setText("...");
        index=-1;
    }
