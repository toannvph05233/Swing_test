    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            //về tự kiểm tra rỗng
            //tự kiểm tra là số
            //tự kiểm miền tuổi 18-65
            //Thêm vào list
            String hoten = txtHoTen.getText();
            int tuoi =Integer.parseInt( txtTuoi.getText());
            String ngoaiNgu = cboNgoaiNgu.getSelectedItem()+"";
            //thêm vào list
            lstHocSinh.add(new HocSinh(hoten, ngoaiNgu, tuoi));
            
            //thêm vào model/bảng <=> fillToTable
            model.addRow(new Object[]{hoten,tuoi,ngoaiNgu});
            
            //xử lý sau khi thêm
            index = lstHocSinh.size()-1;
            showDetail();
            JOptionPane.showMessageDialog(this, "Thêm thành công");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi thêm dữ liệu");
//            e.printStackTrace();
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void tblHocSinhMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblHocSinhMouseClicked
        try {
            index= tblHocSinh.getSelectedRow();
            if(index>=0){
                showDetail();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "lỗi click Table");
        }
    }//GEN-LAST:event_tblHocSinhMouseClicked

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            if (lstHocSinh.size()==0){
                JOptionPane.showMessageDialog(this, "Không còn dữ liệu để xóa");
                return;
            }
            //hỏi xóa
            int hoi = JOptionPane.showConfirmDialog(this, "Bạn muốn xóa thật không?");
            if(hoi!=JOptionPane.YES_OPTION) return;
            
            //thực hiện xóa trong list
            lstHocSinh.remove(index);
            //xóa trong model (table) <=> fillToTable();
            model.removeRow(index);
            //xử lý sau khi xóa
            if(lstHocSinh.size()==0){
                clearForm();
            }else {
                if(lstHocSinh.size()==index) index--;
                showDetail();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "lỗi xóa dữ liệu");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

